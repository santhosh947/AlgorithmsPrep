    private static int min;

    public static int findMin(int[] nums) {
        if (nums == null || nums.length == 0)
            return -1;

        min = Integer.MAX_VALUE;
        binarySearch(nums, 0, nums.length - 1);
        return min;

    }

    private static void binarySearch(int[] n, int l, int r) {
        if (l < 0 || r < 0 || l > n.length - 1 || r > n.length - 1 || l > r)
            return;

        int m = l + (r - l) / 2;
        if (n[m] < min)
            min = n[m];

        if (n[m] < n[r]) {
            binarySearch(n, l, m - 1);
        } else if (n[m] > n[r]) {
            binarySearch(n, m + 1, r);
        } else {
            if (n[l] < min)
                min = n[l];
            binarySearch(n, l + 1, r);
           // binarySearch(n, l, r - 1);
        }
        return;
    }

    public static int findMin2(int[] num) {
        // start with bin search
        int lo = 0;
        int hi = num.length-1;
        int mid = 0;
        while(lo < hi) {
            mid = lo + (hi - lo) / 2;
            
            if (num[mid] > num[hi]) {
                lo = mid + 1;
            }
            else if (num[mid] < num[hi]) {
                hi = mid;
            }
            else { // when num[mid] and num[hi] are same
                hi--;
            }
        }
        
        return num[lo];
        
    }
