    public static boolean search(int[] nums, int target) {
        if(nums==null || nums.length==0)
            return false;
        return binarySearch(nums,0,nums.length-1,target);
    }

    private static boolean binarySearch(int[] nums, int l, int r, int t)
    {
        if(l<0 || l>=nums.length || r<0 || r>=nums.length || l>r)
            return false;
        int mid = l+(r-l)/2;
        if(nums[mid]==t)
            return true;

        if(mid-1>=l && mid+1<=r) {
            if(nums[mid-1]>nums[l])
            {
                if(nums[mid-1]>=t && nums[l]<=t)
                    return binarySearch(nums, l, mid-1, t);
                
                return binarySearch(nums, mid+1,r, t);
            }
            if(nums[mid+1]<nums[r])
            {
                if(nums[mid+1]<=t && nums[r]>=t)
                    return binarySearch(nums, mid+1,r, t);

                return binarySearch(nums, l, mid-1, t);
            }
            if((nums[mid-1]==nums[l]) && (nums[mid+1]==nums[r]))
                return binarySearch(nums, mid+1,r, t) || binarySearch(nums, l, mid-1, t);

        }
        return binarySearch(nums, mid+1,r, t) || binarySearch(nums, l, mid-1, t);
       // return binarySearch(nums, l+1,r, t);

    }
